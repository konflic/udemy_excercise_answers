season = input("Какое сейчас время года?")

if season == "лето" or season == "Лето":
    print("Солнце светит ярче чем когда-либо")
elif season == "зима" or season == "Зима":
    print("За окном падает белый снег")
elif season == "осень" or season == "Осень":
    print("Урожай будет невероятным")
elif season == "весна" or season == "Весна":
    print("Птицы поют прекрасные песни")
else:
    print("Требуется ввести правильное название сезона")
